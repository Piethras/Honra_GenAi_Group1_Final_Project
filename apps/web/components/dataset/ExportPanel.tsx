'use client';

import { useState } from 'react';
import { Download, FileText, Table, Loader2 } from 'lucide-react';

interface Props {
  datasetId: string;
  queries: any[];
  insights: any[];
}

export default function ExportPanel({ datasetId, queries, insights }: Props) {
  const [title, setTitle] = useState('Analysis Report');
  const [selectedQueryIds, setSelectedQueryIds] = useState<string[]>(
    queries.filter(q => q.status === 'SUCCESS').map(q => q.id)
  );
  const [selectedInsightIds, setSelectedInsightIds] = useState<string[]>(
    insights.map(i => i.id)
  );
  const [exporting, setExporting] = useState<'pdf' | 'xlsx' | null>(null);

  const toggle = (id: string, list: string[], setList: (l: string[]) => void) => {
    setList(list.includes(id) ? list.filter(x => x !== id) : [...list, id]);
  };

  const exportPDF = async () => {
  setExporting('pdf');
  try {
    const res = await fetch('/api/export/pdf', {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({
        title,
        queryIds: selectedQueryIds,
        insightIds: selectedInsightIds,
      }),
    });

    if (!res.ok) throw new Error('Export failed');
    const html = await res.text();

    const { jsPDF } = await import('jspdf');
    const doc = new jsPDF({ orientation: 'portrait', unit: 'pt', format: 'a4' });

    const pageWidth = doc.internal.pageSize.getWidth();
    const margin = 40;
    const maxWidth = pageWidth - margin * 2;
    let y = 40;

    const addText = (text: string, size: number, bold: boolean, color: [number, number, number]) => {
      doc.setFontSize(size);
      doc.setFont('helvetica', bold ? 'bold' : 'normal');
      doc.setTextColor(...color);
      const lines = doc.splitTextToSize(text, maxWidth);
      lines.forEach((line: string) => {
        if (y > 780) { doc.addPage(); y = 40; }
        doc.text(line, margin, y);
        y += size * 1.4;
      });
      y += 4;
    };

    // Header
    addText('InsightForge', 10, false, [37, 99, 235]);
    addText(title, 22, true, [17, 24, 39]);
    addText(`Generated on ${new Date().toLocaleDateString('en-US', { year: 'numeric', month: 'long', day: 'numeric' })}`, 10, false, [107, 114, 128]);
    y += 12;

    // Queries
    const selectedQueries = queries.filter(q => selectedQueryIds.includes(q.id));
    if (selectedQueries.length > 0) {
      addText(`Analysis Queries (${selectedQueries.length})`, 14, true, [31, 41, 55]);
      y += 4;
      selectedQueries.forEach(q => {
        addText(`Q: ${q.questionText}`, 11, true, [31, 41, 55]);
        if (q.answerText) addText(q.answerText, 10, false, [55, 65, 81]);
        y += 8;
      });
    }

    // Insights
    const selectedInsights = insights.filter(i => selectedInsightIds.includes(i.id));
    if (selectedInsights.length > 0) {
      y += 8;
      addText(`Key Insights (${selectedInsights.length})`, 14, true, [31, 41, 55]);
      y += 4;
      selectedInsights.forEach(i => {
        addText(i.title, 11, true, [31, 41, 55]);
        addText(i.description, 10, false, [55, 65, 81]);
        addText(`${i.type} · ${i.confidence} confidence`, 9, false, [107, 114, 128]);
        y += 8;
      });
    }

    // Footer
    y += 16;
    addText('Generated by InsightForge · AI-Powered Data Analysis', 9, false, [156, 163, 175]);

    doc.save(`${title.replace(/[^a-z0-9]/gi, '_')}.pdf`);
  } catch (err) {
    alert('PDF export failed. Please try again.');
  } finally {
    setExporting(null);
  }
};

  const exportXLSX = async () => {
    setExporting('xlsx');
    try {
      const res = await fetch('/api/export/xlsx', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({
          title,
          queryIds: selectedQueryIds,
          insightIds: selectedInsightIds,
        }),
      });

      if (!res.ok) throw new Error('Export failed');

      const blob = await res.blob();
      const url = URL.createObjectURL(blob);
      const a = document.createElement('a');
      a.href = url;
      a.download = `${title.replace(/[^a-z0-9]/gi, '_')}.xlsx`;
      a.click();
      URL.revokeObjectURL(url);
    } catch (err) {
      alert('Excel export failed. Please try again.');
    } finally {
      setExporting(null);
    }
  };

  return (
    <div className="max-w-2xl">
      <div className="bg-white border border-gray-200 rounded-xl p-6 mb-4">
        <h3 className="font-semibold text-gray-900 mb-4">Report settings</h3>
        <div>
          <label className="block text-sm font-medium text-gray-700 mb-1">Report title</label>
          <input
            type="text"
            value={title}
            onChange={e => setTitle(e.target.value)}
            className="w-full border border-gray-200 rounded-lg px-3 py-2 text-sm focus:outline-none focus:ring-2 focus:ring-blue-500"
          />
        </div>
      </div>

      {queries.filter(q => q.status === 'SUCCESS').length > 0 && (
        <div className="bg-white border border-gray-200 rounded-xl p-6 mb-4">
          <h3 className="font-semibold text-gray-900 mb-3">Include queries</h3>
          <div className="space-y-2">
            {queries.filter(q => q.status === 'SUCCESS').map(q => (
              <label key={q.id} className="flex items-start gap-3 cursor-pointer group">
                <input
                  type="checkbox"
                  checked={selectedQueryIds.includes(q.id)}
                  onChange={() => toggle(q.id, selectedQueryIds, setSelectedQueryIds)}
                  className="mt-0.5"
                />
                <div>
                  <p className="text-sm font-medium text-gray-800 group-hover:text-blue-600">{q.questionText}</p>
                  {q.answerText && (
                    <p className="text-xs text-gray-400 mt-0.5 line-clamp-1">{q.answerText}</p>
                  )}
                </div>
              </label>
            ))}
          </div>
        </div>
      )}

      {insights.length > 0 && (
        <div className="bg-white border border-gray-200 rounded-xl p-6 mb-6">
          <h3 className="font-semibold text-gray-900 mb-3">Include insights</h3>
          <div className="space-y-2">
            {insights.map(i => (
              <label key={i.id} className="flex items-start gap-3 cursor-pointer group">
                <input
                  type="checkbox"
                  checked={selectedInsightIds.includes(i.id)}
                  onChange={() => toggle(i.id, selectedInsightIds, setSelectedInsightIds)}
                  className="mt-0.5"
                />
                <div>
                  <p className="text-sm font-medium text-gray-800 group-hover:text-blue-600">{i.title}</p>
                  <p className="text-xs text-gray-400 mt-0.5">{i.type} · {i.confidence} confidence</p>
                </div>
              </label>
            ))}
          </div>
        </div>
      )}

      <div className="flex gap-3">
        <button
          onClick={exportPDF}
          disabled={!!exporting || (selectedQueryIds.length === 0 && selectedInsightIds.length === 0)}
          className="flex items-center gap-2 bg-blue-600 text-white px-5 py-2.5 rounded-lg text-sm font-medium hover:bg-blue-700 disabled:opacity-50 transition-colors"
        >
          {exporting === 'pdf' ? <Loader2 className="w-4 h-4 animate-spin" /> : <FileText className="w-4 h-4" />}
          Export PDF
        </button>
        <button
          onClick={exportXLSX}
          disabled={!!exporting || (selectedQueryIds.length === 0 && selectedInsightIds.length === 0)}
          className="flex items-center gap-2 border border-gray-200 text-gray-700 px-5 py-2.5 rounded-lg text-sm font-medium hover:bg-gray-50 disabled:opacity-50 transition-colors"
        >
          {exporting === 'xlsx' ? <Loader2 className="w-4 h-4 animate-spin" /> : <Table className="w-4 h-4" />}
          Export Excel
        </button>
      </div>
    </div>
  );
}